package shapes;

/**
 * like a pair witch contain height and width 
 * 
 */
public class Dimensions {
    public int width;
    public int height;
    /**
     *  Constructor
     * @param width
     * @param height
     */
    public Dimensions(int width, int height) {
        this.width = width;
        this.height = height;
    }
}
